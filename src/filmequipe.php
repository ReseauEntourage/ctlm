    <div class="dip_left">
        <p>
            Ce projet était tout d'abord destiné à prendre la forme d'un court-métrage documentaire. Puis la rencontre avec le journal Le Huffington-Post nous a amené à
            réaliser une web-série autour de Kenny, Loubna et Mickael sur les thèmes du quotidien, de la littérature, de l’amour, de l’homosexualité, de l’architecture. La réussite de cette web-
            série qui a cumulé plus de 150000 vues sur le site du Huffington-Post et été relayée par LCI, Libération, Metronews et d'autres médias nous a par la suite conduit à réaliser un
            film de long métrage. La campagne de crowdfunding et l'aide de Publicis nous a permis de le finaliser.
        </p>
        <p>
            Durant trois ans, nous avons accompagné Kenny, Loubna et Mickael sur leurs lieux de vie, de survie, d’errance, en journée, en soirée, durant la nuit. Nous avons cherché à être à leur écoute, sans jugement, à hauteur d’homme. Avec le temps, la confiance s’installant, ils nous ont confié les aspects plus intimes de leur vie. Le déroulement de ce documentaire suit donc un fil chronologique en même temps qu’il approfondit le propos de ces jeunes .
        </p>
